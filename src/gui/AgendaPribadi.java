    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
     // Menghapus Data pada Tabel
          
          if(tblAgenda.getSelectedRowCount() == 1){
              // Jika satu baris sudah dipilih makan akan terhapus
              model.removeRow(tblAgenda.getSelectedRow());
              
              JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
          }else{
              if(tblAgenda.getRowCount() == 0){
                  // Jika data kosong maka akan muncul pesan 
                  JOptionPane.showMessageDialog(this, "Tabel Datanya Kosong");
              }else{
                  // Jika Tidak Kosong tetapi tidak ada pada baris yang akan dipilih maka muncul pesan
                  JOptionPane.showMessageDialog(this, "Silahkan Pilih Data Yang Mau Dihapus");
              }
          }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEksporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEksporActionPerformed
    // Fungsi untuk mengekspor data ke file
    try{
            BufferedWriter out = new BufferedWriter (new FileWriter("AgendaPribadi.text"));
            
            for(int i = 0; i < tblAgenda.getRowCount(); i++){ // rows
                for (int j = 0; j < tblAgenda.getColumnCount(); j++){ // columns
                    out.write(tblAgenda.getValueAt(i, j).toString()+"/ ");
                }
                out.newLine();
            }            
            JOptionPane.showMessageDialog(null, "Berhasil Diexport Kedalam File");
            
            out.close();
        }catch (Exception e){
            System.err.println("Error: "+e.getMessage());
    }       
    }//GEN-LAST:event_btnEksporActionPerformed

    private void btnImporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImporActionPerformed
    // Mendefinisikan path file yang akan diimpor 
    String filePath = "AgendaPribadi.text";
    File file = new File(filePath);
     try {
    // Membuka file untuk dibaca
    FileReader fr = new FileReader(file);    
    BufferedReader br = new BufferedReader(fr);
    
    // Mendapatkan model tabel yang digunakan
    DefaultTableModel model = (DefaultTableModel) tblAgenda.getModel(); 
    Object[] lines = br.lines().toArray();
    
    // Membaca setiap baris dari file dan menambahkannya ke dalam tabel
    for(int i = 0; i < lines.length; i++ ){
        String[] row = lines[i].toString().split("/ ");
        model.addRow(row);
    }
} catch (IOException e) {
    e.printStackTrace();
}

    }//GEN-LAST:event_btnImporActionPerformed

    private void tblAgendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAgendaMouseClicked
    // Untuk mendapatkan baris yang dipilih saat mouse di-klik pada tabel
         selectedRow = tblAgenda.getSelectedRow();
    // Untuk menampilkan data dari baris yang dipilih pada TextField
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) tblAgenda.getModel();
            tAcara.setText(model.getValueAt(selectedRow, 0).toString());
            jDate.setDate((Date) model.getValueAt(selectedRow, 1));
            cbStart.setSelectedItem(model.getValueAt(selectedRow, 2).toString());
            cbEnd.setSelectedItem(model.getValueAt(selectedRow, 3).toString());
        }
    }//GEN-LAST:event_tblAgendaMouseClicked

