        DefaultTableModel model = (DefaultTableModel) tblAgenda.getModel();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
         model.addRow(new Object[] {Acara, Date, StartTime, EndTime});
